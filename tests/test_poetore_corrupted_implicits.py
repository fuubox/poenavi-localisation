CASES = (
    (
        UNIQUE_DOUBLE,
        {
            "ソケットされた持続時間ジェムのレベル +2",
            "ソケットされた呪いジェムのレベル +2",
        },
    ),
    (
        RARE_DOUBLE,
        {
            "ソケットされたミニオンジェムのレベル +2",
            "ソケットされた持続時間ジェムのレベル +2",
        },
    ),
    (
        UNIQUE_SINGLE,
        {"ソケットされた持続時間ジェムのレベル +2"},
    ),
    (
        RARE_DOUBLE_QUALITY,
        {
            "ソケットされた持続時間ジェムのレベル +2",
            "エンデュランスチャージの最大数 +1",
        },
    ),
)


def test_real_corrupted_samples_preserve_every_corrupted_implicit():
    for text, expected in CASES:
        item = parse_item_text(text)
        parsed = {
            modifier.text
            for modifier in item.modifiers
            if modifier.kind == "implicit" and modifier.generation == "corrupted"
        }
        assert parsed == expected

        filters = resolve_trade_stat_filters(item)
        resolved = {row.text for row in filters}
        assert expected <= resolved


def test_corrupted_quality_sample_keeps_quality_22_filter():
    item = parse_item_text(RARE_DOUBLE_QUALITY)
    filters = resolve_trade_stat_filters(item)
    quality = next(row for row in filters if row.stat_id == "property.quality")

    assert quality.min_value == 22
    assert quality.enabled is True
